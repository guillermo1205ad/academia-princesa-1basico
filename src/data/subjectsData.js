export function getSubjectActivityIds(subject) {
  return ACTIVITY_SECTION_KEYS.flatMap((sectionKey) =>
    subject.sections[sectionKey].map((activity) => activity.id),
  );
}

export function getSubjectTotalActivities(subject) {
  return getSubjectActivityIds(subject).length;
}

export function getGlobalTotalActivities(allSubjects = subjectsData) {
  return allSubjects.reduce((sum, subject) => sum + getSubjectTotalActivities(subject), 0);
}
